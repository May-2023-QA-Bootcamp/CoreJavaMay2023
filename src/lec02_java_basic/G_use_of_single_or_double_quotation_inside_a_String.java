package lec02_java_basic;

// use of " " inside String---> \"  \" 
// use of ' ' inside String ---> \'  \'
// use of \ \  inside String---> \\   \\

public class G_use_of_single_or_double_quotation_inside_a_String {

	public static void main(String[] args) {
		System.out.println("Mark Zuckerberg, American internet entrepreneur and co-founder of Facebook, born in White Plains, New York at 05/14/1984");
		System.out.println("\n--------------------------------------------------------------------------------------------\n");
		System.out.println("\t\"Mark Zuckerberg\", \n\tAmerican internet \\entrepreneur\\ and co-founder of Facebook, \n\tborn in \'White Plains, New York\' at 05/14/1984");
	}

}
